package ru.samsung.jkgroup;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.graphics.Color;
import com.badlogic.gdx.graphics.g2d.BitmapFont;
import com.badlogic.gdx.graphics.g2d.freetype.FreeTypeFontGenerator;

public class FontBuilder {

    public static BitmapFont generate(int size, Color color, String fontPath) {
        FreeTypeFontGenerator generator = new FreeTypeFontGenerator(Gdx.files.internal(fontPath));
        FreeTypeFontGenerator.FreeTypeFontParameter parameter = new FreeTypeFontGenerator.FreeTypeFontParameter();
        parameter.size = size;
        parameter.color = color;
        parameter.characters = "–Р–С–Т–У–Ф–Х–Б–Ц–Ч–Ш–Щ–Ъ–Ы–Ь–Э–Ю–Я–†–°–Ґ–£–§–•–¶–І–®–©–™–Ђ–ђ–≠–Ѓ–ѓ–∞–±–≤–≥–і–µ—С–ґ–Ј–Є–є–Ї–ї–Љ–љ–Њ–њ—А—Б—В—Г—Д—Е—Ж—З—И—Й—К—Л—М—Н—О—П0123456789.,!?-";
        BitmapFont font = generator.generateFont(parameter);
        generator.dispose();
        return font;
    }

}
